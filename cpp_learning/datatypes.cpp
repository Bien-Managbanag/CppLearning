#include <iostream>

using namespace std;

int main()
{
    char grade = 'A'; /*stores a single character*/
    string name = "My name is Bien"; /*can store multiple characters*/
    int age = 21; /*stores a integer*/
    float x = 2.354; /*stores a floating point number*/
    double y = 2.3; /*can store more decimal points than a float*/
    bool isMale = true; /*stores a true or false value*/

    cout << grade << endl;
    cout << name << endl;
    cout << age << endl;
    cout << x << endl;
    cout << y << endl;
    cout << isMale << endl;

    return 0;
}